public class Test11 {
    public static void main(String[] args) {
        //加（+），减（-），乘（*），除（/）,取余(%)
        int x = 10;
        int y = 3;
        System.out.println(x + y);//13
        System.out.println(x - y);//7
        System.out.println(x * y);//30
        System.out.println(x / y);//3,int 类型只能存储整数，小数部分-舍弃
        System.out.println(x % y);//1 取余数
    }
}
