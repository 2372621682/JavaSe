public class Test06 {
    public static void main(String[] args) {
        /**
         * 1.变量的声明并赋值
         * 2.语法：数据类型  变量名称 = 值;
         *    1.数据类型：约束空间存储的数据类型
         *    2.变量名称：空间的名称
         */
        int var = 123;
        System.out.println(var);
        //变量定义后可以多次使用
        System.out.println(var);
    }
}
