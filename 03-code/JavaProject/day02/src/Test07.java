public class Test07 {
    public static void main(String[] args) {
        /**
         * 语法：
         *   1.声明：数据类型  变量名称
         *   2.赋值：变量名称 = 值;
         *  注意：变量，必须先声明，后赋值，再使用
         */
        int var1 ;
        var1 = 123;
        //Variable 'var' might not have been initialized
        System.out.println(var1);

        int var2 = 234;
        System.out.println(var2);
    }
}
