public class Test08 {
    public static void main(String[] args) {
        //语法：数据类型  变量名称1 = 值1,变量名称2 = 值2;
        int var1 = 2,var2 = 3;
        System.out.println(var1);//2
        System.out.println(var2);//3

        var1 = 6;//修改var1空间的值

        System.out.println(var1);//6
        System.out.println(var2);//3

    }
}
