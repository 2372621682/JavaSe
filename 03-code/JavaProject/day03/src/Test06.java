public class Test06 {
    public static void main(String[] args) {
        /**
         * 1.变量必须先声明，后赋值，再使用
         * 2.在同一块区域内变量不能重名
         * 3.同一块区域，现在可以理解为{}包含的部分
         * 4.变量声明后可以多次使用
         */
        int x = 2 ;

        System.out.println(x);
        System.out.println(x);

    }
    int x = 2 ;

}
